CREATE TABLE item (
    id INTEGER PRIMARY KEY AUTOINCREMENT,
    title TEXT NOT NULL,
    date_created DATE NOT NULL DEFAULT CURRENT_DATE,
    date_due DATE NOT NULL DEFAULT CURRENT_DATE,
    description TEXT,
    listid INTEGER,
    completed INTEGER,
    FOREIGN KEY (listid) REFERENCES list (id)
);
